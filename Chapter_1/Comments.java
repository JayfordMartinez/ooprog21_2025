/**
 * This is a documentation comment.
 * It explains what the program does.
 */
public class Comments {
    public static void main(String[] args) {
        
        // This is a single-line comment
        System.out.println("Hello, this shows single-line comments.");
        
        /*
         This is a multi-line comment
         It can take up more than one line
        */
        System.out.println("This shows multi-line comments.");
        
        System.out.println("The text at the top is a documentation comment.");
    }
}